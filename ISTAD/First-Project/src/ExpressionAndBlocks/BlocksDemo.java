package ExpressionAndBlocks;

public class BlocksDemo {
    public static void main(String[] args) {
        String band = "RADOM";
        if (band == "RADOM") {//Stat of block
            System.out.println("HEY!!"+band);
            System.out.println("I miss you so much!!");
        }//end of block
        else {
            System.out.println("Who are you?");
        }
    }
}
