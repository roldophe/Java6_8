package ExpressionAndBlocks;

public class ExpressionDemo {
    public static void main(String[] args) {
        int score;
        score = 90;
//        Here, score 90 is and expression that returns an int.
        Double a = 2.2;
        Double b = 3.4;
        double result;
        result = a + b - 3.4;
//      Here, a+b-3.4 is an expression
        int number1 = 10;
        int number2 = 10;
        if (number1 == number2) {
            System.out.println("number 1 is equal number2.");
        }
//        Here, number1 == number2 is an expression that return a boolean value.
//        Similarly, "number1 is equal number2" is a string expression.
    }
}
