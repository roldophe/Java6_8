package Operators;

public class AssignmentOperators {
    public static void main(String[] args) {
        int value1 = 10;
        value1 += 2; //value1 = value1+2;
        value1 -= 10;//value1 = value1-10 = 2;
        value1 *= 5; //value1 = value1(2)*5 = 10;
        value1 /= 2;  //value1 = value1/2=2;
        System.out.println(value1);


    }
}
