package com.Radom.java.OnlineCourse.Basic.FirstDemo;

public class DemoDataType {

	public static void main(String[] args) {
		
		// BOOLEAN DATA TYPE
		System.out.print("Boolean Type:");
		Boolean flag = true; // or false
		System.out.println(flag);

		// BYTE DATA TYPE
		System.out.print("Byte Type:");
		byte age = 127; // limit -128 to 127
		System.out.println(age);

		// SHORT DATA TYPE
		System.out.print("Short TYPE:");
		Short id = -200; // limit -32768 to 32768
		System.out.println(id);

		// INT DATA TPYE
		System.out.print("Int Data Type:");
		int a = -2500; // limit -2^31 to 2^31 -1
		System.out.println(a);

		// LONG DATA TYPE
		System.out.print("Long Data Type:");
		long l = 254635;
		System.out.println(l);

		// CHAR TYPE
		System.out.println("CHAR TYPE:");
		char c = '\u0052';
		char letter = 68;
		char n = '9';
		System.out.println(c);
		System.out.println(letter);
		System.out.println(n);

		// String TYPE
		System.out.print("String Type:");
		String name = "RADOM KHOEM";
		System.out.println(name);

	}

}
